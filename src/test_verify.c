  int res = mbedtls_rsa_pkcs1_verify( &rsa, NULL, NULL, MBEDTLS_RSA_PUBLIC, MBEDTLS_MD_SHA256, 32, hash, buf );

  printf("-0x%0X\n", -res);

  mbedtls_rsa_free(&rsa);
